using игра_для_проги.Model;

namespace игра_для_проги.Controller
{
    public class MainController
    {
        private SceneModel _model; // Ссылка на модель, где хранятся все точки и линии
        private double _speed = 0.5; // Коэффициент скорости перемещения камеры

        public MainController(SceneModel model)
        {
            _model = model;
        }

        public void CreateTestScene()
        {
            // Очищаем предыдущие данные, чтобы сцена не дублировалась при перезапуске
            _model.Points.Clear();
            _model.Edges.Clear();

            // --- 1. ГАБАРИТЫ КОМНАТЫ (Построение каркаса) ---
            // Точки задаются в формате (X, Y, Z)
            // X: влево(-) и вправо(+), Y: низ(-) и верх(+), Z: от нас вглубь(+)

            // Точки пола (Y = -100)
            _model.AddPoint(-300, -100, 0);   // Точка 0: ближний левый угол
            _model.AddPoint(300, -100, 0);    // Точка 1: ближний правый угол
            _model.AddPoint(300, -100, 600);  // Точка 2: дальний правый угол
            _model.AddPoint(-300, -100, 600); // Точка 3: дальний левый угол

            // Точки потолка (Y = 150)
            _model.AddPoint(-300, 150, 0);    // Точка 4: над точкой 0
            _model.AddPoint(300, 150, 0);     // Точка 5: над точкой 1
            _model.AddPoint(300, 150, 600);   // Точка 6: над точкой 2
            _model.AddPoint(-300, 150, 600);  // Точка 7: над точкой 3

            // Соединяем точки линиями (ребрами) по их индексам
            AddEdge(0, 1); AddEdge(1, 2); AddEdge(2, 3); AddEdge(3, 0); // Периметр пола
            AddEdge(4, 5); AddEdge(5, 6); AddEdge(6, 7); AddEdge(7, 4); // Периметр потолка
            AddEdge(0, 4); AddEdge(1, 5); AddEdge(2, 6); AddEdge(3, 7); // Вертикальные углы стен

            // --- 2. ОКНО (Вытянутое по вертикали) ---
            // Находится на левой стене (X = -300)
            // Высота (Y) теперь от -20 до 60 (итого 80 единиц высоты)
            _model.AddPoint(-300, -20, 260); // Точка 8: нижний ближний угол окна
            _model.AddPoint(-300, -20, 310); // Точка 9: нижний дальний угол окна
            _model.AddPoint(-300, 60, 310);  // Точка 10: верхний дальний угол окна
            _model.AddPoint(-300, 60, 260);  // Точка 11: верхний ближний угол окна
            AddEdge(8, 9); AddEdge(9, 10); AddEdge(10, 11); AddEdge(11, 8); // Рама окна

            // --- 3. ДВЕРЬ (На дальней стене) ---
            // Координата Z всегда 600, так как дверь "в стене"
            _model.AddPoint(-200, -100, 600);  // Точка 12: порог лево
            _model.AddPoint(-110, -100, 600); // Точка 13: порог право
            _model.AddPoint(-110, 90, 600);   // Точка 14: верх право
            _model.AddPoint(-200, 90, 600);    // Точка 15: верх лево
            AddEdge(12, 13); AddEdge(13, 14); AddEdge(14, 15); AddEdge(15, 12); // Контур двери

            // --- 4. ХОЛОДИЛЬНИК (Объемный блок) ---
            // Используем AddBlock(x1, y1, z1, x2, y2, z2) для создания параллелепипеда
            AddBlock(-295, -100, 510, -250, 80, 595);

            // --- 5. БАРНАЯ СТОЙКА (Два блока образуют букву Г) ---
            AddBlock(-250, -100, 380, 150, -35, 450);  // Передняя часть (лицом к залу)
            AddBlock(150, -100, 380, 180, -35, 550); // Боковая часть (вдоль правой стены)

            // --- 6. СТОЛЫ В ЗАЛЕ ---
            AddBlock(-185, -45, 115, -115, -40, 185); // Стол №1 (стоит у окна)
            AddBlock(-180, -100, 175, -175, -40, 180);  // Дальняя левая ножка
            AddBlock(-125, -100, 175, -120, -40, 180);  // Дальняя правая ножка
            AddBlock(-180, -100, 120, -175, -40, 125);    // Ближняя левая ножка
            AddBlock(-125, -100, 120, -120, -40, 125);    // Ближняя правая ножка

            AddBlock(115, -45, 115, 185, -40, 185);    // Стол №2 (у правой стены)
            AddBlock(120, -100, 175, 125, -40, 180);   // Дальняя левая ножка
            AddBlock(175, -100, 175, 180, -40, 180);   // Дальняя правая ножка
            AddBlock(120, -100, 120, 125, -40, 125);   // Ближняя левая ножка
            AddBlock(175, -100, 120, 180, -40, 125);   // Ближняя правая ножка

            // --- 7. СТУЛЬЯ В ЗАЛЕ ---

            // Стулья у стола №1

            // Ближний стул
            AddBlock(-240, -60, 130, -195, -55, 170);  // Сидушка
            AddBlock(-240, -25, 140, -235, -5, 160);  // Верхняя перегородка
            AddBlock(-240, -100, 160, -235, 0, 170);  // Дальняя левая ножка
            AddBlock(-240, -100, 130, -235, 0, 140);  // Ближняя левая ножка
            AddBlock(-200, -100, 130, -195, -55, 140);  // Ближняя правая ножка
            AddBlock(-200, -100, 160, -195, -55, 170);  // Дальняя правая ножка

            // Дальний стул
            AddBlock(-170, -60, 195, -130, -55, 240);  // Сидушка 
            AddBlock(-160, -25, 235, -140, -5, 240);   // Верхняя перегородка 
            AddBlock(-170, -100, 235, -160, 0, 240);   // Дальняя левая ножка 
            AddBlock(-130, -100, 235, -140, 0, 240);   // Дальняя правая ножка
            AddBlock(-170, -100, 195, -160, -55, 200); // Ближняя левая ножка
            AddBlock(-130, -100, 195, -140, -55, 200); // Ближняя правая ножка

            // Стулья у стола №2

            AddBlock(195, -60, 130, 240, -55, 170);   // Сидушка
            AddBlock(240, -25, 140, 245, -5, 160);    // Верхняя перегородка (спинка справа)
            AddBlock(240, -100, 160, 245, 0, 170);    // Ближняя правая ножка
            AddBlock(240, -100, 130, 245, 0, 140);    // Ближняя левая ножка
            AddBlock(195, -100, 130, 200, -55, 140);  // Дальняя левая ножка
            AddBlock(195, -100, 160, 200, -55, 170);  // Дальняя правая ножка

            // --- 8. ПОЛКА С СИРОПАМИ ---
            AddBlock(-295, -40, 400, -250, 80, 490); // Макет насенной полки
            AddBlock(-295, 10, 400, -250, 20, 490); // Полка разделитель внитри основной полки

            // --- 9. ПОЛКА С ЧАЙНЫМИ ПАРАМИ ---
            AddBlock(-20, -50, 593, 20, 80, 598); // Макет насенной полки
            AddBlock(-50, 50, 580, 50, 55, 598); // Верхняя полка разделитель внитри основной полки
            AddBlock(-50, 15, 580, 50, 20, 598); // Средняя полка разделитель внитри основной полки
            AddBlock(-50, -20, 580, 50, -15, 598); // Нижняя полка разделитель внитри основной полки

            // --- 10. НАСТЕННОЕ МЕНЮ ---
            AddBlock(90, -20, 593, 270, 100, 598);

            // --- 11. ТУМБА С ТЕЛЕВИЗОРОМ ---
            AddBlock(-30, -100, 45, 30, -40, 5); // Тумба
            AddBlock(-20, -40, 40, 20, -10, 10); // Телевизор


            // Сдвигаем все объекты по оси Z, чтобы камера не оказалась "внутри" стены при старте
            MoveAll(0, 0, 5);
        }

        // Вспомогательный метод для рисования линии между двумя существующими точками
        private void AddEdge(int startIndex, int endIndex)
        {
            _model.AddEdge(_model.Points[startIndex], _model.Points[endIndex]);
        }

        // Вспомогательный метод для создания 3D-коробки по двум крайним точкам
        private void AddBlock(double x1, double y1, double z1, double x2, double y2, double z2)
        {
            int s = _model.Points.Count; // Запоминаем текущий индекс, с которого начнутся новые точки

            // Создаем 8 вершин куба
            _model.AddPoint(x1, y1, z1); // 0: низ-лево-близко
            _model.AddPoint(x2, y1, z1); // 1: низ-право-близко
            _model.AddPoint(x2, y1, z2); // 2: низ-право-далеко
            _model.AddPoint(x1, y1, z2); // 3: низ-лево-далеко
            _model.AddPoint(x1, y2, z1); // 4: верх-лево-близко
            _model.AddPoint(x2, y2, z1); // 5: верх-право-близко
            _model.AddPoint(x2, y2, z2); // 6: верх-право-далеко
            _model.AddPoint(x1, y2, z2); // 7: верх-лево-далеко

            // Рисуем 12 ребер, соединяя созданные 8 точек
            AddEdge(s + 0, s + 1); AddEdge(s + 1, s + 2); AddEdge(s + 2, s + 3); AddEdge(s + 3, s + 0); // Основание (низ)
            AddEdge(s + 4, s + 5); AddEdge(s + 5, s + 6); AddEdge(s + 6, s + 7); AddEdge(s + 7, s + 4); // Крышка (верх)
            AddEdge(s + 0, s + 4); AddEdge(s + 1, s + 5); AddEdge(s + 2, s + 6); AddEdge(s + 3, s + 7); // Стойки (вертикали)
        }

        // Метод перемещения всей сцены (для управления камерой)
        public void MoveAll(double dx, double dy, double dz)
        {
            foreach (var p in _model.Points)
            {
                p.X += dx * _speed;
                p.Y += dy * _speed;
                p.Z += dz * _speed;
            }
            _model.NotifyChanged(); // Сообщаем View, что нужно перерисовать экран
        }

        // Метод вращения сцены вокруг оси Y
        // Метод вращения всей сцены (вызывается из Form1 при движении мыши)
        // В файле MainController.cs
        public void RotateAll(double angle)
        {
            foreach (var p in _model.Points)
            {
                p.RotateY(angle); // Вызываем метод поворота в каждой точке
            }
            _model.NotifyChanged(); // Генерируем событие, чтобы Form1 перерисовала экран
        }
    }
}